#include <iostream>

using namespace std;

int main()
{
    float x,y,z;
    cin>>x>>y;
    z = x+y;
    cout<<"Sum of these numbers: "<<z<<endl;
    z=x*y;
    cout<<"Product of these numbers: "<<z<<endl;
    z=x/y;
    cout<<"Quotient of these numbers: "<<z<<endl;
    z=x-y;
    cout<<"Difference of these numbers: "<<z<<endl;
}
